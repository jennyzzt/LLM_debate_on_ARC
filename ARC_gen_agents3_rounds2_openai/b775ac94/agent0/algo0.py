def solve(input):
    # Initialize the output grid with zeros, same size as input
    output = [[0 for _ in range(len(input[0]))] for _ in range(len(input))]
    
    # Function to safely set a value in the grid, checking boundaries
    def set_value(grid, x, y, value):
        if 0 <= x < len(grid) and 0 <= y < len(grid[0]):
            grid[x][y] = value
    
    # Iterate through each cell in the input grid
    for i in range(len(input)):
        for j in range(len(input[0])):
            # Copy the current cell value to the output grid
            output[i][j] = input[i][j]
            
            # Apply transformations based on the cell value
            if input[i][j] == 8:
                # Double presence vertically and horizontally
                for dx, dy in [(0, 1), (1, 0), (-1, 0), (0, -1)]:
                    set_value(output, i + dx, j + dy, 8)
            elif input[i][j] == 4:
                # Cross pattern
                for dx, dy in [(0, 1), (1, 0), (-1, 0), (0, -1)]:
                    set_value(output, i + dx, j + dy, 4)
            elif input[i][j] == 3:
                # Double presence vertically
                set_value(output, i + 1, j, 3)
                set_value(output, i - 1, j, 3)
            elif input[i][j] == 2:
                # Double presence horizontally
                set_value(output, i, j + 1, 2)
                set_value(output, i, j - 1, 2)
            elif input[i][j] == 1:
                # Plus pattern
                for dx, dy in [(0, 1), (1, 0), (-1, 0), (0, -1)]:
                    set_value(output, i + dx, j + dy, 1)
    
    return output

