def solve(input):
    # Initialize the output grid with the same dimensions as the input grid
    output = [[0 for _ in range(len(input[0]))] for _ in range(len(input))]
    
    # Apply transformations based on observed rules
    for i in range(len(input)):
        for j in range(len(input[0])):
            # Directly copy the value from input to output
            output[i][j] = input[i][j]
            
            # Check for the [8, 8] pattern and expand vertically
            if i > 0 and input[i][j] == 8 and input[i-1][j] == 8:
                if i + 1 < len(input):
                    output[i+1][j] = 8
            
            # Transform [2, 2] into [4, 4] horizontally
            if j + 1 < len(input[0]) and input[i][j] == 2 and input[i][j+1] == 2:
                output[i][j] = 4
                output[i][j+1] = 4
            
            # Expand 4 horizontally
            if input[i][j] == 4:
                if j + 1 < len(input[0]) and input[i][j+1] == 0:
                    output[i][j+1] = 4
                if j - 1 >= 0 and input[i][j-1] == 0:
                    output[i][j-1] = 4
            
            # Expand 3 vertically
            if input[i][j] == 3:
                if i + 1 < len(input) and input[i+1][j] == 0:
                    output[i+1][j] = 3
                if i - 1 >= 0 and input[i-1][j] == 0:
                    output[i-1][j] = 3
    
    return output

