def solve(input_grid):
    # Initialize the output grid with the same dimensions as the input grid
    output_grid = [[cell for cell in row] for row in input_grid]
    
    # Define the height and width of the grid
    height = len(input_grid)
    width = len(input_grid[0]) if height > 0 else 0
    
    # Function to expand a sequence of numbers in a specific direction
    def expand_sequence(x, y, dx, dy, value):
        while 0 <= x < width and 0 <= y < height and output_grid[y][x] == 0:
            output_grid[y][x] = value
            x += dx
            y += dy
    
    # Iterate through each cell in the input grid
    for y in range(height):
        for x in range(width):
            cell_value = input_grid[y][x]
            
            # Apply expansion rules based on the cell value
            if cell_value == 2:
                expand_sequence(x + 1, y, 1, 0, 2)  # Expand to the right
            elif cell_value == 8:
                expand_sequence(x, y + 1, 0, 1, 8)  # Expand downwards
            elif cell_value == 4 or cell_value == 3:
                # Expand horizontally to both sides
                expand_sequence(x + 1, y, 1, 0, cell_value)
                expand_sequence(x - 1, y, -1, 0, cell_value)
            elif cell_value == 1:
                # Expand into a square pattern if possible
                for dy in range(2):
                    for dx in range(2):
                        if 0 <= x + dx < width and 0 <= y + dy < height:
                            output_grid[y + dy][x + dx] = 1
    
    return output_grid

